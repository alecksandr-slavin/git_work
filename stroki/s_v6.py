n = input()
x = ('а', 'у', 'о', 'ы', 'и', 'э', 'я', 'ю', 'ё', 'е')
y = ('б', 'в', 'г', 'д', 'ж', 'з', 'й', 'к', 'л', 'м', 'н',
     'п', 'р', 'с', 'т', 'ф', 'х', 'ч', 'ц' 'ш', 'щ')
total_0 = 0
total_plus = 0

for i in n:
    let = i.lower()
    if let in x:
        total_0 += 1
    elif let in y:
        total_plus += 1
print('Количество гласных букв равно', total_0)
print('Количество согласных букв равно', total_plus)